class Output:
    def __repr__(self):
        return "Output"

class Loop:
    def __init__(self, body):
        self.body = body
    def __repr__(self):
        return f"Loop({self.body})"

@dataclass
class MachineState:
    def __init__(self, output, tape):
        self.output = output
        self.tape = tape



class BFInterpreter:
    def __init__(self, interactive=False):
        self.interactive = interactive
    def run(self, program, inputs=None):
        tape = [0] * TAPE_LENGTH
        pc = 0
        ptr = 0
        stack = []
        jmp = False
        open = 0
        output = ""
        while True:
            try:
                c = program[pc]
            except IndexError:
                break
            if not jmp:
                if c == ">":
                    ptr = (ptr + 1) % TAPE_LENGTH
                if c == "<":
                    ptr = (ptr - 1) % TAPE_LENGTH
                elif c == "+":
                    tape[ptr] = (tape[ptr] + 1) % 256
                elif c == "-":
                    tape[ptr] = (tape[ptr] - 1) % 256
                elif c == ",":
                    if self.interactive:
                        tape[ptr] = int(input(f"tape[{ptr}] = "))
                    else:
                        tape[ptr] = inputs.pop()
                elif c == ".":
                    if self.interactive:
                        print(chr(tape[ptr]), end="")
                    output += chr(tape[ptr])
            if c == "[":
                if not jmp:
                    if tape[ptr] == 0:
                        jmp = True
                        open = 1
                    else:
                        stack.append(pc)
                else:
                    open += 1
            elif c == "]":
                if not jmp:
                    pc = stack.pop() - 1
                else:
                    open -= 1
                    if not open:
                        jmp = False
            pc += 1
        return MachineState(output, tape)

class Program:
    def __init__(self, source):
        self.source = source
    def __eq__(self, other):
        j = BFInterpreter(interactive=False)
        return j.run(self.source) == j.run(other.source)
    def __lt__(self, other):
        return len(self.source) < len(other.source)
    def __le__(self, other):
        return len(self.source) <= len(other.source)
    def __add__(self, other):
        if isinstance(other, Program):
            return Program(self.source + other.source)
        else:
            return Program(self.source + other)
    def __repr__(self):
        return self.source

def number(n):
    n = n % 256
    if n < 16: 
        return n * "+"
    elif n > 241:
        return "-" * (256 - n)
    return CONSTANTS[n]

def optimize(program):
    new_program = program
    while True:
        new_program = new_program.replace("<>", "")
        new_program = new_program.replace("><", "")
        new_program = new_program.replace("+-", "")
        new_program = new_program.replace("-+", "")
        new_program = new_program.replace("+,", "")
        new_program = new_program.replace("-,", "")
        if new_program == program:
            return new_program
        else:
            program = new_program
    
def put_str(string):
    return Program(optimize(f">{'>'.join(number(ord(c)) for c in string)}[<]>"))

def print_str(string, cleanup=True):
    start = string[0]
    prog = number(ord(start)) + "."
    for c in string[1:]:
        prog += number(ord(c) - ord(start)) + "."
        start = c
    if cleanup:
        prog += "[-]"
    return Program(optimize(prog))

